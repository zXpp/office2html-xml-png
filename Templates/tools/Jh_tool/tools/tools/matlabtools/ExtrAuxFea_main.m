function ExtrAuxFea_main(PathOfAudioInputFiles, CorpusName, PathOfFeatOutputFiles)
% This function is to extract extral auxiliary features from input audio
% files in PathOfAudioInputFiles, and write the extracted feature files to PathOfFeatOutputFiles
%% input parameters
% PathOfAudioInputFiles: the pathe of audio input files
% CorpusName: Corpus name, e.g. 'ami','rt07'
% PathOfFeatOutputFiles: the path of feature output files

[FileName, PrefixName, FileType] = Name_Type(CorpusName);
for i = 1 : length(PrefixName)
    num = 0;
    samples = [];
    filenames = '';
    for j = 1 : length(FileName)
        FileName{j}, PrefixName{i}
        if strfind(FileName{j}, PrefixName{i})
            num = num + 1; %add 1 to number of file
            filenames(num, :) = FileName{j}(1 : end -4); % delete the last 4 characters of file name
            if strcmp(FileType,'wav')
                [x, fs] = wavread(strcat(PathOfAudioInputFiles, '/', FileName{j})); % 
                %% Enframe each channel
                x = single(x);
                windowsize = floor(25 * fs / 1000); %window size is 400 when fs is 16000 (i.e. 25 ms)
                overlap = floor(15 * fs / 1000); %window overlap is 15 ms, shift is 240 when fs is 16000 (i.e. 10 ms)
                samples_enframe{i} = lyx_frame(x, windowsize, overlap, fs);% enframe
                
            elseif strcmp(FileType, 'sph')
                [x, fs] = readsph(strcat(PathOfAudioInputFiles, '/', FileName{j})); %
                %% Enframe each channel
                x = single(x);
                windowsize = floor(25 * fs / 1000); %window size is 400 when fs is 16000 (i.e. 25 ms)
                overlap = floor(15 * fs / 1000); %window overlap is 15 ms, shift is 240 when fs is 16000 (i.e. 10 ms)
                samples_enframe{i} = lyx_frame(x, windowsize, overlap, fs);% enframe                
            end            
        end
    end	
    if (isempty(samples))
        continue;
    else
        [feature] = ExtrAuxFea(samples_enframe, fs);
        clear samples_enframe;    
        for j = 1 : num
            AuxFea = [feature.ne(:,j), feature.log_en_diff(:,j), feature.norm_log_en_diff(:,j), feature.kur(:,j), ...
            feature.mean_norm_cross_corr(:,j), feature.max_norm_cross_corr(:,j),feature.min_norm_cross_corr(:,j), feature.zcr(:,j)]; 
            if strcmp(PathOfFeatOutputFiles(end), '/')
                file = strcat(PathOfFeatOutputFiles, filenames(j, :), '.auxfea');
            else
                file = strcat(PathOfFeatOutputFiles, '/', filenames(j, :), '.auxfea');
            end
            writehtk(file, AuxFea, 0.0100, 9);% writehtk(file,d,fp,tc),
                % d is data array, fp is frame period in seconds, tc is typecode
        end
    end    
end


    




