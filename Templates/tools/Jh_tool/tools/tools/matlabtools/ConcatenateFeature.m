function ConcatenateFeature(PathOfInputFiles, CorpusName, PathOfOutputFiles)
% This function is to extract extral auxiliary features from input audio
% files in PathOfAudioInputFiles, and write the extracted feature files to PathOfFeatOutputFiles
%% input parameters
% PathOfAudioInputFiles: the pathe of audio input files
% CorpusName: Corpus name, e.g. 'ami','rt07'
% PathOfFeatOutputFiles: the path of feature output files

disp('Begin!')
[FileName] = Name_Type(CorpusName);
for i = 1 : length(FileName)
    fprintf('The %d-th file name, %d in total.\n', i, length(FileName));
FileName{i}
    info = getFileName(PathOfInputFiles, FileName{i});  
    data = [];
    framenum =[];
    for j = 1 : length(info)  
        [d{j},fp(j),dt(j),tc(j),t{j}] = readhtk(info(j).path);        
        framenum = [framenum max(size(d{j}))];
    end
    if ~isempty(info)
        clear d fp dt tc t;
        FrameNum = min(framenum) % extract the minimum as the final frame num
    end
    for j = 1 : length(info)  
        [d{j},fp(j),dt(j),tc(j),t{j}] = readhtk(info(j).path);
        data = [data d{j}(1 : FrameNum, :)];
    end
    if ~isempty(info)
        file = info(1).name;
        file(strfind(info(1).name, '.') : end) = []; %extract file name
        file = strcat(PathOfOutputFiles, '/', file, '.allfea');
        if sum(fp) == fp(1) * length(fp) % if frame shift is same
            tc = 9; % the feature is USER
            writehtk(file, data, fp(1), tc);
        else
            error('Frame shift of different features are not same!');
        end
    end
    
end
disp('All feature concatenations are Finished!')


